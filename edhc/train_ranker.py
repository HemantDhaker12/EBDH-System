def load_processed_candidates(processed_dir: Path) -> list:
    """Load all normalized candidate files from the processed directory."""
    candidates = []
    for file_path in processed_dir.glob("*.json"):
        try:
            with open(file_path, "r", encoding="utf-8") as f:
                data = json.load(f)
            candidates.append(CandidateProfileNormalized(**data))
        except Exception as e:
            logger.error(f"Failed to load processed candidate {file_path.name}: {e}")
    return candidates

def load_candidates_from_file(file_path: Path, max_samples: int = 2000) -> list:
    """Load candidates from a JSON or JSONL file (either raw or gzipped)."""
    import gzip
    candidates = []
    logger.info(f"Loading up to {max_samples} candidates from {file_path}...")
    
    open_func = gzip.open if file_path.suffix == ".gz" else open
    mode = "rt" if file_path.suffix == ".gz" else "r"
    
    # Read file content to detect if it is a JSON array or JSONL
    with open_func(file_path, mode, encoding="utf-8") as f:
        first_char = ""
        for char in f.read(100):
            if char.strip():
                first_char = char
                break
                
    # Parse based on structure
    with open_func(file_path, mode, encoding="utf-8") as f:
        if first_char == "[":
            # standard JSON array
            data = json.load(f)
            for item in data[:max_samples]:
                candidates.append(CandidateProfile(**item))
        else:
            # JSONL format
            count = 0
            for line in f:
                if not line.strip():
                    continue
                data = json.loads(line)
                candidates.append(CandidateProfile(**data))
                count += 1
                if count >= max_samples:
                    break
                    
    logger.info(f"Loaded total {len(candidates)} candidates from {file_path.name}.")
    return candidates

